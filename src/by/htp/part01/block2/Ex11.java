/*
 * 11. Составить программу, которая определит площадь какого треугольника больше.
 */
public class Ex11 {

	public static void main(String[] args) {
		int abc1 = 12;
		int abc2 = 43;

		if (abc1 > abc2) {
			System.out.println("Площадь треугольника abc1 больше");
		} else {
			System.out.println("Площадь треугольника abc2 больше");
		}
	}
}
